#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c > 255 || c < 0)
		return ((char *)s);
	if (s == NULL)
		return (NULL);
	if (c == '\0')
		return ((char *)s + ft_strlen(s));
	if (s == NULL)
		return (NULL);
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	return (NULL);
}
